package aggregation;

public class Address {
	
	String HouseName;
	String PostOffice;
	String District;
	int Pin;
	
	
	public Address(String HouseName, String PostOffice, String District, int Pin) {
		
		this.HouseName=HouseName;
		this.PostOffice=PostOffice;
		this.District=District;
		this.Pin=Pin;
		
	}
}
